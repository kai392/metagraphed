import { execFileSync } from "node:child_process";
import { readFileSync } from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";
import {
  artifactRelativePath,
  isGeneratedPublicArtifactRelativePath,
} from "../src/artifact-storage.mjs";
import { artifactFilePath } from "./lib.mjs";

// Generated artifacts like openapi.json now exceed Node's default 1 MB
// execFileSync buffer, so git reads must allow plenty of headroom or the
// output overflows and throws ENOBUFS.
const GIT_OUTPUT_MAX_BUFFER = 64 * 1024 * 1024;

function main() {
  const changedFilesPath = valueAfter("--changed-files") || "changed-files.txt";
  const artifactRoot = valueAfter("--artifact-root") || "public/metagraph";

  const GENERATED_PUBLIC_ARTIFACTS = new Set([
    // build-summary.json moved to R2-only (#1003); only r2-manifest + types.d.ts
    // remain committed generated artifacts.
    "public/metagraph/r2-manifest.json",
    "public/metagraph/types.d.ts",
  ]);

  const submittedArtifacts = readFileSync(changedFilesPath, "utf8")
    .split(/\r?\n/)
    .map((file) => file.trim().replace(/\\/g, "/"))
    .filter(isSubmittedPublicArtifactPath);

  if (submittedArtifacts.length === 0) {
    console.log("No submitted public artifacts to verify.");
    process.exit(0);
  }

  const submittedDatasets = submittedArtifacts.filter((file) =>
    file.startsWith("public/datasets/"),
  );
  if (submittedDatasets.length > 0) {
    console.error(
      [
        "Do not commit public/datasets files; they are regenerated on build/deploy.",
        "Unexpected submitted dataset artifact(s):",
        ...submittedDatasets.map((file) => `- ${file}`),
      ].join("\n"),
    );
    process.exit(1);
  }

  for (const artifact of loadIndexedArtifacts(artifactRoot)) {
    if (isGeneratedPublicArtifactRelativePath(artifact)) {
      GENERATED_PUBLIC_ARTIFACTS.add(`public/metagraph/${artifact}`);
    }
  }

  const unknownArtifacts = submittedArtifacts.filter(
    (file) => !GENERATED_PUBLIC_ARTIFACTS.has(file),
  );

  if (unknownArtifacts.length > 0) {
    console.error(
      [
        "Submitted public artifacts must be generated by the compact artifact build.",
        "Remove arbitrary public/metagraph files or add them to the reviewed artifact contracts.",
        "Do not commit public/datasets files; they are regenerated on build/deploy.",
        "Unexpected submitted artifact(s):",
        ...unknownArtifacts.map((file) => `- ${file}`),
      ].join("\n"),
    );
    process.exit(1);
  }

  // Compare each submitted artifact against a freshly-built one by CONTENT, not bytes. A submitter
  // builds on a different OS/Node than CI (ubuntu/Node 22), so byte-exact `git diff` flags benign
  // cross-platform JSON-formatting differences (object key order and number rendering).
  // We normalize both sides by recursively sorting object keys while preserving array order,
  // because JSON array order is semantic for public artifacts and API responses.
  const mismatches = [];
  for (const artifact of submittedArtifacts) {
    if (!artifact.endsWith(".json")) {
      // Non-JSON generated files (e.g. types.d.ts) keep the strict byte check.
      if (gitDiffsFromHead(artifact)) {
        mismatches.push(`${artifact} (byte mismatch)`);
      }
      continue;
    }
    let committed;
    let rebuilt;
    try {
      committed = JSON.parse(gitShow(`HEAD:${artifact}`));
    } catch {
      mismatches.push(`${artifact} (committed version unreadable)`);
      continue;
    }
    try {
      rebuilt = JSON.parse(readFileSync(artifact, "utf8"));
    } catch {
      mismatches.push(`${artifact} (rebuilt version unreadable)`);
      continue;
    }
    if (
      canonicalArtifactJson(artifact, committed) !==
      canonicalArtifactJson(artifact, rebuilt)
    ) {
      mismatches.push(`${artifact} (content differs from a fresh build)`);
    }
  }

  if (mismatches.length > 0) {
    console.error(
      [
        "Submitted public artifacts do not match a fresh build (content differs, not just formatting):",
        ...mismatches.map((file) => `- ${file}`),
        "Run `npm run build` and commit the regenerated public artifacts.",
      ].join("\n"),
    );
    process.exit(1);
  }
  console.log(
    "Submitted public artifacts are reproducible (content-equivalent to a fresh build).",
  );
}

if (
  process.argv[1] &&
  path.resolve(process.argv[1]) === fileURLToPath(import.meta.url)
) {
  main();
}

export function isSubmittedPublicArtifactPath(file) {
  return (
    file.startsWith("public/metagraph/") || file.startsWith("public/datasets/")
  );
}

/** Stable JSON: recursively sort object keys while preserving semantic array order. */
export function canonicalJson(value) {
  return JSON.stringify(normalizeForComparison(value));
}

export function canonicalArtifactJson(artifactPath, value) {
  const normalized = normalizeForComparison(value);
  if (
    artifactPath === "public/metagraph/r2-manifest.json" &&
    normalized &&
    typeof normalized === "object" &&
    !Array.isArray(normalized)
  ) {
    // The committed compact manifest is a publish lockfile, but its full/R2-only
    // byte totals can vary slightly across otherwise-equivalent rebuilds. Keep
    // the dual artifact entries strict while ignoring the known unstable R2
    // aggregate byte counters only when they are valid byte totals; otherwise
    // committed public manifest tampering must remain visible to the verifier.
    if (isValidByteTotal(normalized.full_artifact_size_bytes)) {
      delete normalized.full_artifact_size_bytes;
    }
    if (
      normalized.storage_tier_size_bytes &&
      typeof normalized.storage_tier_size_bytes === "object" &&
      !Array.isArray(normalized.storage_tier_size_bytes)
    ) {
      if (isValidByteTotal(normalized.storage_tier_size_bytes.r2)) {
        delete normalized.storage_tier_size_bytes.r2;
      }
    }
  }
  return JSON.stringify(normalized);
}

function isValidByteTotal(value) {
  return Number.isSafeInteger(value) && value >= 0;
}

function normalizeForComparison(value) {
  if (Array.isArray(value)) {
    return value.map(normalizeForComparison);
  }
  if (value && typeof value === "object") {
    const out = Object.create(null);
    for (const key of Object.keys(value).sort()) {
      out[key] = normalizeForComparison(value[key]);
    }
    return out;
  }
  return value;
}

function gitShow(ref) {
  return execFileSync("git", ["show", ref], {
    encoding: "utf8",
    stdio: ["ignore", "pipe", "pipe"],
    maxBuffer: GIT_OUTPUT_MAX_BUFFER,
  });
}

function gitDiffsFromHead(file) {
  try {
    execFileSync("git", ["diff", "--exit-code", "--", file], {
      encoding: "utf8",
      stdio: "pipe",
      maxBuffer: GIT_OUTPUT_MAX_BUFFER,
    });
    return false;
  } catch (error) {
    if (error.code === "ENOBUFS") {
      throw error;
    }
    return true;
  }
}

function loadIndexedArtifacts(rootPath) {
  const artifactPaths = new Set();
  // build-summary.json is R2-only (#1003) — read its index from the staging tier.
  for (const artifact of readJsonArtifact(
    artifactFilePath("build-summary.json"),
  ).artifacts || []) {
    const relativePath = artifactRelativePath(artifact.path || "");
    if (relativePath) {
      artifactPaths.add(relativePath);
    }
  }
  for (const artifact of readJsonArtifact(
    path.join(rootPath, "r2-manifest.json"),
  ).artifacts || []) {
    const relativePath = artifactRelativePath(artifact.path || "");
    if (relativePath) {
      artifactPaths.add(relativePath);
    }
  }
  return artifactPaths;
}

function readJsonArtifact(filePath) {
  return JSON.parse(readFileSync(filePath, "utf8"));
}

function valueAfter(flag) {
  const index = process.argv.indexOf(flag);
  if (index === -1) {
    return null;
  }
  return process.argv[index + 1] || null;
}
